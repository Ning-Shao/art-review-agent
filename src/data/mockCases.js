export const defaultMockCase = mockCases[0];

export function getMockCaseById(caseId) {
  return mockCases.find((item) => item.id === caseId) || null;
}

function countMatches(text, words) {
  return words.reduce((count, word) => count + (word && text.includes(word) ? 1 : 0), 0);
}

export function findBestMockCase(work = {}) {
  const explicitCase = getMockCaseById(work.caseId);
  if (explicitCase) return explicitCase;

  const source = [
    work.title,
    work.category,
    work.track,
    work.theme,
    work.description,
    work.breadcrumb,
    work.mediaNode,
  ].filter(Boolean).join(' / ');

  if (!source.trim()) return defaultMockCase;

  const ranked = mockCases.map((item) => {
    const keywords = [
      item.title,
      item.category,
      item.theme,
      item.track,
      item.workType,
      item.mediaNode,
      ...item.theme.split(/[、/ ]/),
      ...item.category.split(/[、/ ]/),
    ];
    const exactTitle = work.title && work.title === item.title;
    const isDefaultTitle = work.title === defaultMockCase.title || work.title === '城市更新海报方案';
    const isDefaultNode = !work.breadcrumb || work.breadcrumb === defaultMockCase.breadcrumb;
    const titleBoost = exactTitle ? (isDefaultTitle && !isDefaultNode ? 4 : 18) : 0;
    const nodeBoost = source.includes(item.breadcrumb) ? 14 : 0;
    return {
      item,
      score: titleBoost + nodeBoost + countMatches(source, keywords),
    };
  }).sort((a, b) => b.score - a.score);

  return ranked[0] && ranked[0].score > 0 ? ranked[0].item : defaultMockCase;
}
